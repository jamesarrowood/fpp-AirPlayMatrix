<div style="margin:0 auto;">
  <h3>AirPlay Video to Matrix Help</h3>
  <p>
    1) Configure your matrix in <b>Input/Output Setup -> Pixel Overlay Models</b>.<br />
    2) In this plugin, set <b>Matrix Model Name</b> exactly to that overlay model name.<br />
    3) Save config and click <b>Start</b>.<br />
    4) On iPhone/iPad/Mac, pick the configured AirPlay receiver name and start screen/video mirroring.
  </p>
  <p>
    If orientation is wrong, use <b>Flip Horizontally</b> / <b>Flip Vertically</b> and restart.
  </p>
</div>
